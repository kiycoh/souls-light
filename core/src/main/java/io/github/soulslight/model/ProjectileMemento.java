package io.github.soulslight.model;

public class ProjectileMemento {
    public float x;
    public float y;
    public float vx;
    public float vy;

    public ProjectileMemento() {}

    public ProjectileMemento(float x, float y, float vx, float vy) {
        this.x = x;
        this.y = y;
        this.vx = vx;
        this.vy = vy;
    }
}
